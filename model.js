class Contact {
  id;
  name;
  email;
  telephone;
  landline;
  webaddress;
  address;
  constructor(args) {
    this.id = args.id;
    this.name = args.name;
    this.email = args.email;
    this.telephone = args.telephone;
    this.landline = args.landline;
    this.webaddress = args.webaddress;
    this.address = args.address;
  }
}